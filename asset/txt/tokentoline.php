<h1>What is TokenToLine?</h1>
<p>TokenToLine is a crypto project that
    aims to develop the most open source
    bep-20 token. We want to be as transparent
    as possible and therefore have everything
    visible to our users.</p>

<p> There are special functions in the contract (The crypto token)
    that ensure that we as a team cannot manipulate
    or change anything. In addition, there are wallet
    addresses that are banned from the holders fee
    so that more holders fee remains for the holders.
    Think of our entire team and all liquidity pools.</p>

<p>Besides all this, our token is also linked to BNB.
	This ensures that if bnb rises, TTL would increase with the same
    percentage.</p>



<h2>Liquidity pool</h2>
<p>Liquidity pool
    5% of every trade is invested in the
    Liquidity pool. This ensures that the
    price would always rise slightly.
    It also ensures that the token has a
    better stable price.</p>
<p>We automatically burn our lp tokens so
    that the ratio in our liquidity pool always
    remains the same. This keeps the price
    stable and it would not be possible to
    manipulate the market.</p>
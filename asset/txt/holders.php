<h2>Mining fee</h2>
<p>Our token cannot be minted. We did this
    so that no tokens were added and because the
    mining process is very bad for the climate.
    In order to earn money, we have a holder's fee.
    Anyone who has a TTL token in their Wallet will
    receive a small reward for each transaction
    that takes place. Per transaction with the
    token TTL, 2% is deducted. This is a
    reward that is divided in percentage among
    everyone who has TTL tokens in his Wallet.</p>

<p>To do this fairly, we have removed all: liquidity pools,
    development marketing wallet and owner from the holders
    rewards. This means that there are many more rewards
    left for the holders of the token TTL.</p>
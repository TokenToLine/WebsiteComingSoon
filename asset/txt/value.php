<h2>Value</h2>
<p>Our TTL token is linked to BNB.
    This means that the token TTL moves with the
    BNB price. The TTL token has a limited
    supply, which ensures that there is a
    scarcity among the TTL tokens. This
    will drive the price higher on top of
    BNB's price.</p>

<p>We have done this to make
    it more interesting for investors.</p>


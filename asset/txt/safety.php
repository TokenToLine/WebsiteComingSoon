<h2>Safety</h2>
<p>Security is very important to us, that’s
why you own the token and we don’t work
like the normal banking system. You have
full ownership of the token and we cannot
do anything with your money.</p>

<p>It works like the following. You have your
own wallet. Because of this, only you are
the one who has access to your money
(crypto). Unlike a bank. The bank
manages your money and you are not
the direct owner of your money.</p>

<p>In addition, our coin is built on a stable
and very reliable blockchain network.</p>

<p>Furthermore, we have a dev wallet. If
something ever happens or goes wrong,
we can reimburse the tokens from the
dev wallet.</p>
                <p>Blockchain technology has been called the
                    greatest invention after the internet, but
                    what is it exactly? A blockchain is a type
                    of database in which information can be
                    stored. You can think of it as small blocks
                    of information that are added to a long
                    chain. Each new block that is added
                    contains information about the previous
                    and current block (such as time, price,
                    wallet address, etc.). If the old and new
                    block do not match, it will not be
                    connected to the chain.</p>
                    
                <h2>How does blockchain work</h2>
                
                <p>This chain is therefore getting longer and
                    can never be adjusted again. Every
                    transaction is saved forever. This is why
                    this technology can be used to carry out
                    transactions. The advantage of a
                    blockchain is that every transaction that
                    has ever been made can be traced back
                    forever, and cannot be undone in any way.
                    With a blockchain it is therefore possible
                    to permanently record a transaction,
                    without the need for a third party.</p>
